package api.payloads;



public class Student {
	
	    private int Id;
	    public String Name;
	    public String Address;
		
	    public int getId() {
			return Id;
		}
		public void setId(int id) {
			Id = id;
		}
		public String getName() {
			return Name;
		}
		public void setName(String name) {
			Name = name;
		}
		public String getAddress() {
			return Address;
		}
		public void setAddress(String address) {
			Address = address;
		}
	
	  
		
	
	    
	    
		
	   	
	

}
